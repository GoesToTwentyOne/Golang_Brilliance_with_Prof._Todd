package main

import "fmt"

var x int
var y string
var z float64

func main() {
	fmt.Printf("value of x is: %v\n", x)
	fmt.Printf("Type of x is: %T\n", x)
	fmt.Printf("value of y is: %v\n", y)
	fmt.Printf("type of y is: %T\n", y)
	fmt.Printf("value of z is: %v\n", z)
	fmt.Printf("type of z is: %T\n", z)

}
